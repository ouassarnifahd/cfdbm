static CvMemStorage* storage = 0;
static CvHaarClassifierCascade* cascade = 0;

void detect_and_draw( IplImage* image );

const char* cascade_name =
    "haarcascade_frontalface_alt.xml";
/*    "haarcascade_profileface.xml";*/

int main( int argc, char** argv )
{

	IplImage *image = 0;

    int optlen = strlen("--cascade=");
    const char* input_name;

    if( argc > 1 && strncmp( argv[1], "--cascade=", optlen ) == 0 )
    {
        cascade_name = argv[1] + optlen;
        input_name = argc > 2 ? argv[2] : 0;
    }
    else
    {
        cascade_name = "/usr/share/opencv/haarcascades/haarcascade_frontalface_alt.xml";
        input_name = argc > 1 ? argv[1] : 0;
    }

    cascade = (CvHaarClassifierCascade*)cvLoadHaarClassifierCascade( cascade_name, cvSize(640, 480) );

    if( !cascade )
    {
        fprintf( stderr, "ERROR: Could not load classifier cascade\n" );
        fprintf( stderr,
        "Usage: facedetect --cascade=\"<cascade_path>\" [filename]\n" );
        return -1;
    }
    storage = cvCreateMemStorage(0);

    if( !input_name )
    {
        fprintf( stderr, "ERROR: Need a valid filename\n" );
        fprintf( stderr,
        "Usage: facedetect --cascade=\"<cascade_path>\" [filename]\n" );
        return -1;
    }

    image = cvLoadImage( input_name , 1);

	if (!image) {
		printf("Could not load image file: %s\n", input_name);
		exit(0);
	}

    cvNamedWindow( "result", 1 );

	detect_and_draw( image );

	cvWaitKey( 0 );

    cvReleaseImage( &image );

    cvDestroyWindow("result");

    return 0;
}

void detect_and_draw( IplImage* img )
{
    static CvScalar colors[] =
    {
        {{0,0,255}},
        {{0,128,255}},
        {{0,255,255}},
        {{0,255,0}},
        {{255,128,0}},
        {{255,255,0}},
        {{255,0,0}},
        {{255,0,255}}
    };

    double scale = 1.3;
    IplImage* gray = cvCreateImage( cvSize(img->width,img->height), 8, 1 );
    IplImage* small_img = cvCreateImage( cvSize( cvRound (img->width/scale),
                         cvRound (img->height/scale)),
                     8, 1 );
    int i;

    cvCvtColor( img, gray, CV_BGR2GRAY );
    cvResize( gray, small_img, CV_INTER_LINEAR );
    cvEqualizeHist( small_img, small_img );
    cvClearMemStorage( storage );

    if( cascade )
    {
        double t = (double)cvGetTickCount();
        CvSeq* faces = cvHaarDetectObjects( small_img, cascade, storage,
                                            1.1, 2, 0/*CV_HAAR_DO_CANNY_PRUNING*/,
                                            cvSize(60, 60), cvSize(200, 200) );
        t = (double)cvGetTickCount() - t;
        printf( "detection time = %gms\n", t/((double)cvGetTickFrequency()*1000.) );
        for( i = 0; i < (faces ? faces->total : 0); i++ )
        {
            CvRect* r = (CvRect*)cvGetSeqElem( faces, i );
            CvPoint center;
            CvPoint A, B;
            A.x = cvRound(r->x * scale);
            A.y = cvRound(r->y * scale);
            B.x = cvRound((r->x + r->width)*scale);
            B.y = cvRound((r->y + r->height)*scale);
            int radius;
            center.x = cvRound((r->x + r->width*0.5)*scale);
            center.y = cvRound((r->y + r->height*0.5)*scale);
            // radius = cvRound((r->width + r->height)*0.25*scale);
            // cvCircle( img, center, radius, colors[i%8], 3, 8, 0 );
            cvRectangle( img, A, B, colors[i%8], 2, 8, 0);
        }
    }

    cvShowImage( "result", img );

    cvReleaseImage( &gray );
    cvReleaseImage( &small_img );
}
